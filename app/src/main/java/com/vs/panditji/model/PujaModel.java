package com.vs.panditji.model;

public class PujaModel {


    /**
     * id : 1
     * name : Engagement Puja Sagai
     * price : 5000
     * img : pooja.png
     */

    private String id;
    private String name;
    private String price;
    private String img;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
